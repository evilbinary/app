// 判断当前位置前面(或后面)的文字是否汉字
// 是汉字返回TRUE，不是汉字返回FALSE
// cString:字符串
// iPos:当前位置的索引(第几个字节)
// bMode:TRUE检查后面的两个字节;FALSE检查前面的两个字节
BOOL IsChinese(char* cString, int iPos, BOOL bMode)
{

#if defined(CHINESE_SUPPORT)
	int iLength = strlen (cString);
	unsigned char byCharacter[3];
	memset (byCharacter, 0x0, 3);

	if (bMode)
	{
		// 取后面两个字节
		if ((iPos < 0) || (iPos > iLength-2))
		{
			return FALSE;
		}
		memcpy( byCharacter, (cString + iPos), 2 );
	}
	else
	{
		// 取前面两个字节
		if ((iPos < 2) || (iPos > iLength))
		{
			return FALSE;
		}
		memcpy( byCharacter, (cString + iPos - 2), 2 );
	}

	// 判断cCharacter是否汉字
	if( ((byCharacter[0] > 0x80) && (byCharacter[0] < 0xff)) &&
	   (((byCharacter[1] > 0x3f) && (byCharacter[1] < 0x7f)) ||
		((byCharacter[1] > 0x7f) && (byCharacter[1] < 0xff))) )
		return TRUE;
#endif // defined(CHINESE_SUPPORT)

	return FALSE;
}

// 判断当前位置是否塞在了汉字的两个字节中间
// 塞在中间返回FALSE，否则返回TRUE
BOOL IsPosValid(char* cString, int iPos)
{
	// 算法：
	// 从头往后一个一个的过滤，如果找到一个等于iPos的位置，
	// 说明这个位置是合理的，否则这个位置是不合理的
	if (iPos == 0)
		return TRUE;

	int iLength = strlen (cString);

	int i;
	int iValidPos = 0;
	for (i=0; i<iLength; i++)
	{
		if (IsChinese (cString, iValidPos, TRUE))
			iValidPos += 2;
		else
			iValidPos ++;

		if (iValidPos == iPos)	// 说明这个位置是合理的
			return TRUE;
		
		if (iValidPos > iPos)
			return FALSE;
	}

	return FALSE;
}

// 得到一个字符串的显示长度
int GetDisplayLength(char* cString, int iLength)
{
	int iStrLength = strlen(cString);
	if (iStrLength > iLength)
		iStrLength = iLength;

	int iDisplayLength = 0;
	int iValidPos = 0;

	int i;
	for (i=0; i<iStrLength; i++)
	{
		if (IsChinese (cString, iValidPos, TRUE))
		{
			iValidPos += 2;
			iDisplayLength += (HZK_W + 1);
		}
		else
		{
			iValidPos ++;
			iDisplayLength += (ASC_W + 1);
		}

		if (iValidPos >= iStrLength)
			return iDisplayLength;
	}

	return iDisplayLength;
}

// 给出一个宽度和一个字符串，得到可以显示的字节数
int GetDisplayLimit(char* cString, int iWidth)
{
	if(iWidth <= 0)
		return 0;

	int iStrLength = strlen(cString);
	int iByteCount = 0;
	int iDisplayLength = 0;
	int i = 0;
	while( i <= iStrLength )
	{
		if (IsChinese (cString, i, TRUE))
		{
			i += 2;
			iDisplayLength += (HZK_W + HZK_GAP);
		}
		else
		{
			i += 1;
			iDisplayLength += (ASC_W + ASC_GAP);
		}

		if (iDisplayLength > iWidth)
			return iByteCount;
		else
			iByteCount = i;
	}
	return iByteCount;
}

