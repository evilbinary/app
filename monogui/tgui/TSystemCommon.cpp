static int _gb2312(unsigned char *str)
{
	return ((str[0] > 0xa0) && (str[0] < 0xff) &&
			(str[1] > 0xa0) && (str[1] < 0xff));
}
static int _gb2312_index(unsigned char *s)
{
	return (s[0] - 0xa1) * (0xfe - 0xa1 + 1) + s[1] - 0xa1;
}
static int _gb13000(unsigned char *str)
{
	return (((str[0] > 0x80) && (str[0] < 0xff)) &&
			(((str[1] > 0x3f) && (str[1] < 0x7f)) ||
			((str[1] > 0x7f) && (str[1] < 0xff))));
}
static int _gb13000_index(unsigned char *s)
{
    register unsigned int s0, s1;
    s0 = s[0];
    s1 = s[1];
    if (s0 < 0x0a1)
	{
		if (s1 < 0x07f)    
			return (s0 - 0x081) * 0x0be + (s1 - 0x040);
		if (s1 < 0x0ff)
			return (s0 - 0x081) * 0x0be + (0x03f + s1 - 0x080);
    }
	if (s0 < 0x0aa)
	{
		if (s0 != 0x0a8 && s0 != 0x0a9)
			return -(1410 + 6768);
		if (s1 >= 0x40 && s1 <= 0x0a0 && s1 != 0x7f)
		{
			if (s1 >= 0x40 && s1 < 0x07f)
			{
				return -(1410 + 6768) + 846 + s1 - 0x40 + ((s0 == 0x0a8) ? 0:96);
			}
			else
				return -(1410 + 6768) + 846 + s1 - 0x40 - 1 + ((s0 == 0x0a8) ? 0:96);
		}
		else
			return -(1410 + 6768);
    }
	else
	{
		if (s1 < 0x07f)
			return 0x020 * 0x0be + (s0 - 0x0aa) * 0x060 + (s1 - 0x040);
		if (s1 < 0x0a1)
			return 0x020 * 0x0be + (s0 - 0x0aa) * 0x060 + (0x03f + s1 - 0x080);
    }

	// 程序不会跑到这里
	return -(1410 + 6768);
}
static int _hz_index(unsigned char *str)
{
    if (_gb2312(str))
	return _gb2312_index(str);

    if (_gb13000(str))
	return 1410 + 6768 + _gb13000_index(str);

    return -1;
}
